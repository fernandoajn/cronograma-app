    private void btnMakeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMakeActionPerformed
        // TODO add your handling code here:
        try{
            FileWriter arquivo;
            
            if (true && !(lista.isEmpty()) ) {
               arquivo = new FileWriter(new File("rotina_"+nome+".txt"), true);
               arquivo.write("Cronograma do dia "+data+"\r\n");
               arquivo.write("=========================================================================="+"\r\n");
               ArrayList<String>tasks = new ArrayList();
               
                    for (int i = 0; i < lista.getSize(); i++) {
                        tasks.add((String) lista.getElementAt(i));
                        arquivo.write(tasks.get(i) + "\r\n");
                        }
                    
               arquivo.write("=========================================================================="+"\r\n");
               arquivo.write("\r\n");
               arquivo.write("\r\n");
               arquivo.close();
               ProcessBuilder pb = new ProcessBuilder("Notepad.exe", "rotina_"+nome+".txt");
               pb.start();
               
            }else{
                System.out.println("Erro ao tentar gerar arquivo!");
                JOptionPane.showMessageDialog(null, "Lista vazia!", "Erro", JOptionPane.WARNING_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro ao tentar gerar arquivo!", "Erro fatal", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnMakeActionPerformed

